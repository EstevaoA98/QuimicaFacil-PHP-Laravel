                    <article class="blog-post">
                        <h2 class="display-5 link-body-emphasis mb-1">Experimento Químico: <br> Crescendo Cristais de
                            Sulfato de Cobre</h2>
                        <p class="blog-post-meta">2021 by <a href="https://edu.rsc.org" target="_blank" rel="external">Royal
                                Society of Chemistry </a></p>

                        <strong>
                            <p>Objetivo:
                                Demonstrar o processo de cristalização por evaporação e obter cristais visíveis de sulfato
                                de cobre.</p>
                        </strong>
                        <h1>Materiais Necessários</h1>
                        <p>
                        <h3>1. Reagentes:</h3>
                        <ul>
                            <li>Sulfato de cobre pentahidratado () - Pode ser encontrado em lojas de produtos químicos ou de
                                jardinagem (como fungicida).</li>
                            <li>CuSO4⋅5H2OCuSO_4 \cdot 5H_2O</li>
                            <li>Água destilada (para evitar impurezas que interfiram na cristalização). </li>
                        </ul> <br>
                        <h3>2. Equipamentos:</h3>
                        <ul>
                            <li>Béquer ou copo de vidro resistente ao calor.</li>
                            <li>Bastão de vidro ou colher de madeira para mexer.</li>
                            <li>Fogareiro ou aquecedor elétrico (nunca direto no fogo).</li>
                            <li>Placa de Petri ou pires (para permitir a evaporação).</li>
                            <li>Fio de náilon ou barbante (para servir como base para os cristais).</li>
                            <li>Suporte para pendurar o fio (palito ou lápis que fique na borda do copo).</li>
                        </ul>
                        <br>
                        <hr>

                        <h1>Preparação e Instruções</h1>
                        <h3>1. Dissolução:</h3>
                        <ul>
                            <li>Aqueça cerca de 200 ml de água destilada até que esteja quente, mas não fervendo.</li>
                            <li>Adicione lentamente sulfato de cobre à água quente, mexendo até que não se dissolva mais
                                (solução saturada). O líquido deve ficar azul intenso.</li>
                        </ul>
                        <br>
                        <h3>2. Filtragem (opcional):</h3>
                        <ul>
                            <li>Se observar partículas não dissolvidas, coe a solução em um filtro de papel (por exemplo,
                                filtro de café) para remover impurezas.</li>
                        </ul>
                        <br>
                        <h3>3. Montagem do sistema de cristalização:</h3>
                        <ul>
                            <li>Transfira a solução para outro recipiente limpo, como um copo de vidro.</li>
                            <li>Pendure um fio de náilon ou barbante preso a um palito ou lápis na boca do recipiente, de
                                forma que fique suspenso dentro da solução sem tocar nas laterais.</li>
                        </ul>
                        <br>
                        <h3>4. Evaporação controlada:</h3>
                        <ul>
                            <li>Deixe o recipiente em local seguro e sem vibrações, preferencialmente longe de luz solar
                                direta.</li>
                            <li>Após 24-48 horas, a evaporação da água levará à formação de cristais no fio e nas laterais
                                do recipiente.</li>
                        </ul>
                        <br>
                        <hr>
                        <img src="img/imgPost/experiment-qui.webp" alt="">
                        <hr>
                        <br>
                        <h3><strong>Dicas para Sucesso</strong></h3>
                        <li><strong>Evaporação lenta</strong>: Coloque um pano poroso sobre o recipiente para evitar
                            evaporação muito rápida e proteger contra poeira.</li>
                        <li><strong>Controle de temperatura:</strong> Faça isso em um ambiente com temperatura estável para
                            obter cristais maiores.</li>
                        <li><strong>Repetição:</strong> Se desejar cristais maiores, remova os primeiros cristais formados e
                            reintroduza-os na solução saturada como "sementes".
                        </li>
                        <hr>
                        <h3><strong>Cuidados e Segurança</strong></h3>
                        <li>Não ingira o sulfato de cobre. Ele é tóxico em contato com a pele ou se ingerido.</li>
                        <li>Lave bem as mãos após manipular os reagentes.</li>
                        <li>Use óculos de proteção ao aquecer a solução.</li>
                        <hr>
                        <h3><strong>Resultados Esperados</strong></h3>
                        <li>Após alguns dias, você verá cristais azuis brilhantes crescendo no fio e nas laterais do
                            recipiente.</li>
                        <li>Você pode observar as faces geométricas dos cristais e entender o processo de organização das
                            moléculas durante a cristalização.</li>
